package org.advyteam.otherClasses;

public class FileParams {
  public String fileName;
  public String regenaratedFileName;
  public String fullPath;

  public FileParams() {
  }

  public String getFileName() {
    return fileName;
  }

  public void setFileName(String fileName) {
    this.fileName = fileName;
  }

  public String getRegenaratedFileName() {
    return regenaratedFileName;
  }

  public void setRegenaratedFileName(String regenaratedFileName) {
    this.regenaratedFileName = regenaratedFileName;
  }

  public String getFullPath() {
    return fullPath;
  }

  public void setFullPath(String fullPath) {
    this.fullPath = fullPath;
  }
}
